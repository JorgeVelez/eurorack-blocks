#include "Kivu12.h"

#include <cmath>



/*
==============================================================================
Name : process
==============================================================================
*/

void  Kivu12::process ()
{
   osc.set_freq (440.f);

   for (size_t i = 0 ; i < erb_BUFFER_SIZE ; ++i)
   {
      float val = ui.b16.held () ? osc.process () : 0.f;

      ui.audio_out1 [i] = val;
      ui.audio_out2 [i] = val;
   }
}
