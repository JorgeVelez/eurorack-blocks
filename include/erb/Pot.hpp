/*
==============================================================================
Name : ctor
==============================================================================
*/

template <FloatRange Range>
Pot <Range>::Pot (const float & data)
:  impl_data (data)
{
}



/*
==============================================================================
Name : operator float
==============================================================================
*/

template <FloatRange Range>
Pot <Range>::operator float () const
{
   if constexpr (Range == FloatRange::Normalized)
   {
      return impl_data;
   }
   else if constexpr (Range == FloatRange::Bipolar)
   {
      return impl_data * 2.f - 1.f;
   }
}



