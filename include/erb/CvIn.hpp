/*
==============================================================================
Name : ctor
==============================================================================
*/

template <FloatRange Range>
CvIn <Range>::CvIn (const float & data)
:  _data (data)
{
}



/*
==============================================================================
Name : operator float
==============================================================================
*/

template <FloatRange Range>
CvIn <Range>::operator float () const
{
   if constexpr (Range == FloatRange::Normalized)
   {
      return _data;
   }
   else if constexpr (Range == FloatRange::Bipolar)
   {
      return _data * 2.f - 1.f;
   }
}



