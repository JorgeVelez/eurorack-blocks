/*
==============================================================================
Name : ctor
==============================================================================
*/

Switch::Switch (const uint8_t & data_0, const uint8_t & data_1)
:  _0 (data_0)
,  _1 (data_1)
{
}



/*
==============================================================================
Name : operator Position
==============================================================================
*/

Switch::operator Position () const
{
   if (_0.held ())
   {
      return Position::Out0;
   }
   else if (_1.held ())
   {
      return Position::Out1;
   }
   else
   {
      return Position::Center;
   }
}



