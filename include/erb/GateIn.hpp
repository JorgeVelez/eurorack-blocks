/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : ctor
==============================================================================
*/

GateIn::GateIn (const uint8_t & data)
:  _data (data)
{
}



/*
==============================================================================
Name : triggered
==============================================================================
*/

bool  GateIn::triggered () const
{
   return _current && !_previous;
}



/*
==============================================================================
Name : operator bool
==============================================================================
*/

GateIn::operator bool () const
{
   return _current;
}



/*\\\ INTERNAL \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : impl_preprocess
==============================================================================
*/

void  GateIn::impl_preprocess ()
{
   _previous = _current;
   _current = _data != 0;
}



