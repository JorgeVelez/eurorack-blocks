/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : ctor
==============================================================================
*/

AudioOutDaisy::AudioOutDaisy (Module & module)
:  _module (module)
{
   module.add (*this);
}



/*
==============================================================================
Name : size
==============================================================================
*/

size_t   AudioOutDaisy::size () const
{
   return buffer_size;
}



/*
==============================================================================
Name : operator []
==============================================================================
*/

AudioOutDaisy::Frame &   AudioOutDaisy::operator [] (size_t index)
{
   switch (index)
   {
   case 0:
   default:
      return left;

   case 1:
      return right;
   }
}



/*\\\ INTERNAL \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : impl_notify_audio_buffer_start
==============================================================================
*/

void  AudioOutDaisy::impl_notify_audio_buffer_start ()
{
   // nothing
}



/*
==============================================================================
Name : impl_notify_audio_buffer_end
==============================================================================
*/

void  AudioOutDaisy::impl_notify_audio_buffer_end ()
{
   auto & buffer = _module.impl_onboard_codec_buffer_output ();

   buffer [0] = left;
   buffer [1] = right;
}



