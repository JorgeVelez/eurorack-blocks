/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : operator float
==============================================================================
*/

CvIn::operator float () const
{
   return *_norm_val_ptr * 2.f - 1.f;
}



/*\\\ INTERNAL \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : impl_bind
==============================================================================
*/

void  CvIn::impl_bind (float & norm_val)
{
   _norm_val_ptr = &norm_val;
}



