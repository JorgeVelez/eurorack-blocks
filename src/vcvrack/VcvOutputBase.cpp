/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : ctor
==============================================================================
*/

VcvOutputBase::VcvOutputBase (VcvModule & module)
{
   module.add (*this);
}



/*
==============================================================================
Name : index
==============================================================================
*/

size_t   VcvOutputBase::index () const
{
   return _index;
}



/*
==============================================================================
Name : set_norm_val
==============================================================================
*/

void  VcvOutputBase::set_norm_val (float norm_val)
{
   _output_ptr->setVoltage (norm_val * 5.f);
}



/*\\\ INTERNAL \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : impl_bind
==============================================================================
*/

void  VcvOutputBase::impl_bind (size_t index, rack::engine::Output & output)
{
   _index = index;
   _output_ptr = &output;
}



