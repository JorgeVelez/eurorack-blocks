#include "VoiceTransient.h"

#include <cmath>



/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : trigger
==============================================================================
*/

void  VoiceTransient::trigger ()
{
   _active_flag = true;
   _pos = 0;
   _ramp.reset (1.f);
}



/*
==============================================================================
Name : stop
==============================================================================
*/

void  VoiceTransient::stop ()
{
   _ramp = 0.f;
}



/*
==============================================================================
Name : set_speed
==============================================================================
*/

void  VoiceTransient::set_speed (float step_spl)
{
   _step_spl = step_spl;
}
