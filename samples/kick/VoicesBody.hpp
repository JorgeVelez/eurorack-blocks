/*
==============================================================================
Name : process
==============================================================================
*/

template <typename T>
float VoicesBody::process (const T & sample)
{
   float val = 0.f;
   for (auto && voice : _voices)
   {
      val += voice.process (sample);
   }

   return val;
}
