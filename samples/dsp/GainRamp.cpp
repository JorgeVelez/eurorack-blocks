/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : process
==============================================================================
*/

void  GainRamp::process ()
{
   constexpr float step = 1.f / 256.f;

   if (_cur < target)
   {
      _cur += step;
      if (_cur > target) _cur = target;
   }
   else if (_cur > target)
   {
      _cur -= step;
      if (_cur < target) _cur = target;
   }
}



